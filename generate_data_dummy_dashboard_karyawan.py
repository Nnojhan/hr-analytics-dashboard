"""2024"""

import pandas as pd
import numpy as np
import openpyxl
from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
from openpyxl.utils import get_column_letter

# ---- 1. READ DATAFRAME YANG DIBUTUHKAN ----
try:
    df_presensi = pd.read_excel("Presensi_PT_ABC.xlsx")
    df_lembur = pd.read_excel("Data_Lembur_PT_ABC.xlsx")
except FileNotFoundError as e:
    print(f"Eror: File master tidak ditemukan! {e}")
    raise

# Konversi kolom tanggal ke format datetime
df_presensi['Tanggal Check In'] = pd.to_datetime(df_presensi['Tanggal Check In'])
df_lembur['Tanggal Mulai'] = pd.to_datetime(df_lembur['Tanggal Mulai'])

summary_data = []

# Loop untuk memproses tahun 2024 dan 2025
for tahun in [2024, 2025]:
    # Filter data berdasarkan tahun
    df_p_tahun = df_presensi[df_presensi['Tanggal Check In'].dt.year == tahun].copy()
    df_l_tahun = df_lembur[(df_lembur['Tanggal Mulai'].dt.year == tahun) & (df_lembur['Status'] == 'Disetujui')].copy()

    if df_p_tahun.empty:
        continue

    # --- A. PERHITUNGAN PERSENTASE KEHADIRAN ---
    # Total hari efektif kerja adalah hari di mana statusnya bukan Libur Nasional
    total_hari_efektif = df_p_tahun[df_p_tahun['Remarks'] != 'Libur Nasional'].shape[0]

    # Karyawan dianggap hadir jika statusnya WFO, Terlambat, Perjalanan Dinas, atau WFO (Lembur Pengganti)
    total_kehadiran_riil = df_p_tahun[df_p_tahun['Remarks'].isin(['WFO', 'Perjalanan Dinas', 'Terlambat', 'WFO (Lembur Pengganti)'])].shape[0]

    if total_hari_efektif > 0:
        pct_kehadiran = (total_kehadiran_riil / total_hari_efektif) * 100
    else:
        pct_kehadiran = 0

    # --- B. PERHITUNGAN OVERTIME (TOTAL DURASI DESIMAL) ---
    total_overtime_durasi = df_l_tahun['Durasi Lembur'].sum()

    # --- C. PERHITUNGAN JUMLAH TOTAL KASUS ABSENSI ---
    total_terlambat = df_p_tahun[df_p_tahun['Remarks'] == 'Terlambat'].shape[0]
    total_sakit = df_p_tahun[df_p_tahun['Remarks'].str.contains('Sakit', na=False)].shape[0]
    total_izin_full = df_p_tahun[df_p_tahun['Remarks'] == 'Izin Satu Hari'].shape[0]
    total_izin_half = df_p_tahun[df_p_tahun['Remarks'] == 'Izin Setengah Hari'].shape[0]

    # --- D. SIMULASI DEVIASI POPULASI (BERDASARKAN AGREGAT BULANAN) ---
    # Agar nilai deviasi masuk akal terhadap total tahunan, kita hitung standar deviasi dari variasi tren bulanan
    df_p_tahun['Bulan'] = df_p_tahun['Tanggal Check In'].dt.month
    df_l_tahun['Bulan'] = df_l_tahun['Tanggal Mulai'].dt.month

    bulanan_p = df_p_tahun.groupby('Bulan').agg(
        Hadir=('Remarks', lambda x: x.isin(['WFO', 'Perjalanan Dinas', 'Terlambat', 'WFO (Lembur Pengganti)']).sum()),
        Efektif=('Remarks', lambda x: (x != 'Libur Nasional').sum()),
        Terlambat_Count=('Remarks', lambda x: (x == 'Terlambat').sum()),
        Sakit_Count=('Remarks', lambda x: x.str.contains('Sakit', na=False).sum()),
        Izin_Count=('Remarks', lambda x: (x == 'Izin Satu Hari').sum()),
        Izin_Half_Count=('Remarks', lambda x: (x == 'Izin Setengah Hari').sum())
    )
    bulanan_p['Pct'] = (bulanan_p['Hadir'] / bulanan_p['Efektif']) * 100
    bulanan_l = df_l_tahun.groupby('Bulan')['Durasi Lembur'].sum()

    # Hitung standar deviasi (di-scale ke proporsi tahunan agar tidak timpang)
    std_kehadiran = bulanan_p['Pct'].std() if len(bulanan_p) > 1 else 0
    std_overtime = bulanan_l.std() * 2.5 if len(bulanan_l) > 1 else 0
    std_terlambat = bulanan_p['Terlambat_Count'].std() * 2.5 if len(bulanan_p) > 1 else 0
    std_sakit = bulanan_p['Sakit_Count'].std() * 2.5 if len(bulanan_p) > 1 else 0
    std_izin_full = bulanan_p['Izin_Count'].std() * 2.5 if len(bulanan_p) > 1 else 0
    std_izin_half = bulanan_p['Izin_Half_Count'].std() * 2.5 if len(bulanan_p) > 1 else 0

    summary_data.append({
        'Tahun': tahun,
        'Rata-Rata Kehadiran': round(pct_kehadiran, 2),
        'Deviasi Kehadiran': round(std_kehadiran, 2),
        'Overtime': round(total_overtime_durasi, 1),
        'Deviasi Overtime': round(std_overtime, 2),
        'Terlambat': int(total_terlambat),
        'Deviasi Terlambat': round(std_terlambat, 2),
        'Sakit': int(total_sakit),
        'Deviasi Sakit': round(std_sakit, 2),
        'Izin Full Day': int(total_izin_full),
        'Deviasi Izin Full Day': round(std_izin_full, 2),
        'Izin Setengah Hari': int(total_izin_half),
        'Deviasi Izin Setengah Hari': round(std_izin_half, 2)
    })

df_summary_final = pd.DataFrame(summary_data)

