            p_remarks = ['WFO', 'Terlambat', 'Perjalanan Dinas', 'Sakit SKD', 'Sakit Non-SKD', 'Cuti Tahunan', 'Izin Setengah Hari', 'Izin Satu Hari', 'Cuti Khusus']
