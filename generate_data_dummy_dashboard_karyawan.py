try:
    df_karyawan = pd.read_excel("Data_Karyawan_PT_ABC.xlsx")
    df_lembur = pd.read_excel("Data_Lembur_PT_ABC.xlsx")
except FileNotFoundError as e:
    print(f"Eror: File master atau data lembur tidak ditemukan! {e}")
    raise

# Konversi kolom tanggal ke format datetime
df_karyawan['Tanggal Masuk'] = pd.to_datetime(df_karyawan['Tanggal Masuk'])
df_karyawan['Tanggal Resign'] = pd.to_datetime(df_karyawan['Tanggal Resign'], errors='coerce')
df_lembur['Tanggal Mulai'] = pd.to_datetime(df_lembur['Tanggal Mulai'])

# ---- 2. ATURAN PASAR GAJI BERDASARKAN UMK Jakarta ----
UMK_Jakarta_2025 = 5729876

# Bobot pengali dasar per level jabatan
level_multipliers = {
    'Operator': 1.0,
    'Staff': 1.4,
    'Supervisor': 2.2,
    'Manager': 4.0,
    'Direksi': 7.0
}

def hitung_gaji_pokok_individu(row, tahun_analisis=2025):
    level = row['Level Position']
    tgl_masuk = row['Tanggal Masuk']

    # Gaji Dasar = Multiplier Level * UMK
    gaji_dasar = level_multipliers.get(level, 1.0) * UMK_Jakarta_2025

    # Hitung masa kerja (Tahun) hingga tahun 2025
    masa_kerja_tahun = max(0, tahun_analisis - tgl_masuk.year)

    # Kenaikan loyalitas/pengalaman kerja (3.5% per tahun masa kerja)
    gaji_akhir = gaji_dasar * (1 + (masa_kerja_tahun * 0.035))

    # Tambahkan sedikit variasi acak industri (noise +- 5%) agar data natural
    variasi = np.random.uniform(0.95, 1.05)

    return round(gaji_akhir * variasi, -3) # Dibulatkan ke ribuan terdekat

# Terapkan perhitungan gaji individu sementara ke df_karyawan
df_karyawan['Gaji_2025'] = df_karyawan.apply(hitung_gaji_pokok_individu, axis=1)

# ---- 3. PROSES AGREGASI BULANAN PERIODE 2025 ----
payroll_summary = []

for bulan in range(1, 13):
    awal_bulan = pd.Timestamp(datetime.date(2025, bulan, 1))
    if bulan == 12:
        akhir_bulan = pd.Timestamp(datetime.date(2025, 12, 31))
    else:
        akhir_bulan = pd.Timestamp(datetime.date(2025, bulan + 1, 1)) - pd.Timedelta(days=1)

    nama_bulan = awal_bulan.strftime('%B 2025')

    # A. Hitung Total Gaji Karyawan yang AKTIF di bulan tersebut
    # Karyawan dihitung berhak digaji jika: Tanggal Masuk <= Akhir Bulan DAN (Tanggal Resign kosong ATAU Tanggal Resign >= Awal Bulan)
    # Karyawan dengan status 'Fiktif' diabaikan
    df_aktif_bulan_ini = df_karyawan[
        (df_karyawan['Status'] != 'Fiktif') &
        (df_karyawan['Tanggal Masuk'] <= akhir_bulan) &
        ((df_karyawan['Tanggal Resign'].isna()) | (df_karyawan['Tanggal Resign'] >= awal_bulan))
    ]

    total_salary_bulan_ini = df_aktif_bulan_ini['Gaji_2025'].sum()

    # B. Hitung Total Kompensasi Lembur Berstatus DISETUJUI pada bulan tersebut
    df_lembur_bulan_ini = df_lembur[
        (df_lembur['Status'] == 'Disetujui') &
        (df_lembur['Tanggal Mulai'] >= awal_bulan) &
        (df_lembur['Tanggal Mulai'] <= akhir_bulan)
    ].copy()

    # Hitung biaya lembur per baris kejadian
    total_overtime_pay_bulan_ini = 0
    for _, row_lembur in df_lembur_bulan_ini.iterrows():
        nrp_lembur = row_lembur['NRP']
        durasi = row_lembur['Durasi Lembur']
        tipe_hari = row_lembur['Weekend/Weekday']

        # Cari komponen gaji pokok individu pemohon lembur untuk menentukan tarif per jam
        info_karyawan = df_karyawan[df_karyawan['NRP'] == nrp_lembur]
        if not info_karyawan.empty:
            gapok = info_karyawan['Gaji_2025'].values[0]
        else:
            gapok = UMK_CIMAHI_2025

        # Rumus upah lembur per jam standar Depnaker RI (Gapok / 173)
        tarif_per_jam_dasar = gapok / 173.0

        # Penentuan faktor pengali berdasarkan tipe hari kerja/libur
        if tipe_hari == 'Weekend':
            biaya_per_kejadian = durasi * tarif_per_jam_dasar * 2.0 # Pengali weekend lebih besar
        else:
            biaya_per_kejadian = durasi * tarif_per_jam_dasar * 1.5

        total_overtime_pay_bulan_ini += biaya_per_kejadian

    payroll_summary.append({
        'Bulan': nama_bulan,
        'Total Salary': round(total_salary_bulan_ini, -3),
        'Total Kompensasi Lembur': round(total_overtime_pay_bulan_ini, -3)
    })

