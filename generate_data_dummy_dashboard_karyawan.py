# ---- 2. FUNGSI PENJADWALAN KUOTA PERIODE (Maks Masuk 3, Keluar 1) ----
def get_periode_key(date):
    # Cut-off tanggal 21. Jika > 21, masuk ke periode bulan berikutnya
    if date.day > 21:
        if date.month == 12:
            return (date.year + 1, 1)
        return (date.year, date.month + 1)
    return (date.year, date.month)

# Tracker kuota per periode
