def validasi_dan_set_tanggal(tipe="masuk", tahun_start=2015, tahun_end=2025):
    while True:
        thn = random.randint(tahun_start, tahun_end)
        bln = random.randint(1, 12)
        tgl = random.randint(1, 28)
        dt = datetime.date(thn, bln, tgl)
        p_key = get_periode_key(dt)

        if tipe == "masuk":
            if kuota_masuk.get(p_key, 0) < 3:
                kuota_masuk[p_key] = kuota_masuk.get(p_key, 0) + 1
                return dt
        elif tipe == "keluar":
            if kuota_keluar.get(p_key, 0) < 1:
                kuota_keluar[p_key] = kuota_keluar.get(p_key, 0) + 1
                return dt

